<h2>Závěr</h2>
<p>Studenti se během kurzu seznámili se základy práce na konfokálním mikroskopu,   s problematikou migrace B-lymfocytů a s tématem chronické lymfocytární leukemi. Praktická část pak byla věnována přípravě buněčných preparátů pro mikroskopii a nácviku práce na konfokálním mikroskopu. Praktický kurz byl ukončem písemným testem, v němž studenti prokázali vědomosti získané během kurzu. Všichni studenti test úspěšně složili.</p>
