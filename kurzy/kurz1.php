<h1>PRAKTICKÝ KURZ Č. 1</h1>

<h1>Analýza migrace lymfocytů v gradientu</h1>

<h2>Vyučující: Mgr. Markéta Kaucká</h2>
<h2>Datum konání: 11.-12.4.2012</h2>

<p>Tento praktický kurz s názvem "Analýza migrace lymfocytů v gradientu" byl uskutečněn rámci aktivity KA 2 podle původního plánu projektu.  Cílem kurzu bylo seznámit studenty s  možnostmi studia migrace lymfocytů, ale i jiných buněk v gradientu biologicky aktivních látek (např. Chemokinů) za požití metody Dunnovy komůrky a snímání pohybu živých, fluorescenčně značených buněk na konfokálním mikroskopu. Kurz proběhl v laboratoři dr. Bryji na ústavu experimentální biologie.</p>
<p>Kurzu se zúčastnilo 6 pregraduálních studentů (Valnohová, Janovská, Hutňan, Harnoš, Paclíková, Kotrbová) 2 doktorští studenti (Červenka, Sri Ganji) a 2 postdoci (Pospíchalová, Bernatík).</p>


